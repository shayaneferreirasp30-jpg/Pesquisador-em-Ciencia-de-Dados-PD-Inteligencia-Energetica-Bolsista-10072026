# Cobertura: necessidade real de crédito
q1["co"] = np.maximum(
    q1["eca"] - (q1["cr"] + q1["d"]),
    0
)

# Percentual de rateio
soma_co = q1["co"].sum()

if soma_co > 0:
    q1["p"] = q1["co"] / soma_co
