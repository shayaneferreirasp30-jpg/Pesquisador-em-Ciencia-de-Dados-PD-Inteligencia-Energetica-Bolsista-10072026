def redistribuir_credito(
    df,
    credito_disponivel
):
    resultado = df.copy()

    resultado["credito_recebido"] = 0.0

    # Ordena pela segurança:
    # maior score primeiro
    ordem = resultado.sort_values(
        [
            "score_seguranca",
            "capacidade_receber"
        ],
        ascending=[False, False]
    ).index

    restante = float(credito_disponivel)

    for indice in ordem:
        if restante <= 0:
            break

        capacidade = float(
            resultado.loc[
                indice,
                "capacidade_receber"
            ]
        )

        if capacidade <= 0:
            continue

        alocacao = min(
            capacidade,
            restante
        )

        resultado.loc[
            indice,
            "credito_recebido"
        ] = alocacao

        restante -= alocacao

    return resultado, restante


