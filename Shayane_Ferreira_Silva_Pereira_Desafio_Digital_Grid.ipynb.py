def metricas(y_real, y_prev):
    y_real = np.asarray(y_real, dtype=float)
    y_prev = np.asarray(y_prev, dtype=float)

    mae = np.mean(np.abs(y_real - y_prev))

    # O MAPE não é adequado quando o valor real é zero.
    mascara = y_real != 0

    if mascara.any():
        mape = np.mean(
            np.abs(
                (y_real[mascara] - y_prev[mascara])
                / y_real[mascara]
            )
        ) * 100
    else:
        mape = np.nan

    wmape = (
        np.sum(np.abs(y_real - y_prev))
        / np.sum(np.abs(y_real))
        * 100
        if np.sum(np.abs(y_real)) > 0
        else np.nan
    )

    return mae, mape, wmape


def criar_features(df, alvo, grupo=None):
    if grupo:
        ordenacao = [grupo, MES]
    else:
        ordenacao = [MES]

    dados = df.sort_values(ordenacao).copy()

    if grupo:
        for lag in [1, 2, 3, 6, 12]:
            dados[f"lag_{lag}"] = (
                dados.groupby(grupo)[alvo].shift(lag)
            )

        dados["media_3m"] = (
            dados.groupby(grupo)[alvo]
            .transform(lambda x: x.shift(1).rolling(3).mean())
        )

        dados["media_6m"] = (
            dados.groupby(grupo)[alvo]
            .transform(lambda x: x.shift(1).rolling(6).mean())
        )

    else:
        for lag in [1, 2, 3, 6, 12]:
            dados[f"lag_{lag}"] = dados[alvo].shift(lag)

        dados["media_3m"] = (
            dados[alvo].shift(1).rolling(3).mean()
        )

        dados["media_6m"] = (
            dados[alvo].shift(1).rolling(6).mean()
        )

    dados["mes_numero"] = dados[MES].dt.month
    dados["mes_seno"] = np.sin(
        2 * np.pi * dados["mes_numero"] / 12
    )
    dados["mes_cosseno"] = np.cos(
        2 * np.pi * dados["mes_numero"] / 12
    )

    dados["tendencia"] = (
        dados[MES].dt.year * 12
        + dados[MES].dt.month
    )

    return dados


